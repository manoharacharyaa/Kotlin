package com.example.kotlinbasics
//Arithmetic Operators (| + | - | * | / | % |)
fun main(){

    var result1 = 5 + 3
    println(result1)
    var result2 = 5 - 3
    println(result2)
    var result3 = 5 * 3
    println(result3)
    var result4 = 15 / 3
    println(result4)
    var result5 = 15 % 3
    println(result5)

    var result6 = 5 + 3
    result6 *= 2
    println(result6)

    val a = 5.0
    val b = 3
    val result7: Double = (a / b)
    println(result7)
}