package com.example.kotlinbasics
// var & val
fun main(){
    var myName = "Manohar"
    myName = "Virat"
    println("Hello " + myName)

    val mySurname = "Acharya"
    mySurname = "Kohli"
    println("Hello " + mySurname)
}

//Diff between var & val
//var can be overwritten
//val can't be overwritten

